        <h1>Données publiques en France : Girondins VS Montagnards ?</h1>
    </header>
    <div class="body">
        <p>
            La proposition n°22 du rapport Riester suggère la création d’une plateforme d’innovation de services ci-nommée «&nbsp;Etat Lab&nbsp;» qui permettrait aux acteurs tiers de développer des services innovants à partir de données publiques.</p>
        <p>
            La semaine dernière, <a href="http://twitter.com/Nicolasvoisin" title="Twitter de Nicolas Voisin">@nicolasvoisin</a>
            indiquait sur Twitter la création prochaine d’un état lab en France.
            Je n’avais pas réussi à trouver la source de l’information
            mais c’est chose faite grâce à
            <a href="http://twitter.com/epsiplatform" title="Twitter du European Public Sector Information Platform">@epsiplatform</a>,
            il s’agit de la page 38
            du <a href="http://www.scribd.com/doc/27236592/Rapport-Riester">rapport Riester</a>
            remis la semaine dernière à 
            <a href="http://fr.wikipedia.org/wiki/%C3%89ric_Woerth">Éric Woerth</a>
            et <a href="http://fr.wikipedia.org/wiki/Nathalie_Kosciusko-Morizet">Nathalie Kosciusko-Morizet</a>.
        </p>
        <p><strong>État lab</strong></p>
        <p>
            La proposition n°22 du rapport Riester suggère la création d’une plateforme d’innovation de services ci-nommée «&nbsp;Etat Lab&nbsp;» qui permettrait aux acteurs tiers de développer des services innovants à partir de données publiques
            <sup><a href="#ref-1">1</a></sup>.
        </p>
        <p>
            Cette plateforme prendrait appui
            sur le futur portail national de
            l’<a href="https://www.apiefrance.com/"><acronym title="Agence du Patrimoine Immatériel de l’État">APIE</acronym></a>
            de mise à disposition des données publiques
            et serait initialisée fin 2010.
            Elle permettrait d’intégrer les bases de données et API,
            ce qui rappelle trait pour trait
            le principe du <a href="http://www.data.gov/catalog">datagov</a> aux Etats-Unis (notamment).
        </p>
        <p>
            Ce que le rapport ne dit pas :
            <ul>
                <li>Quelles données libère-t-on ?</li>
                <li>Sous quel format ?</li>
                <li>Quels seront les types de licences pour l’usage de ces données ?</li>
                <li>Les formats seront-ils user-friendly (usage citoyen) et/ou mashup friendly (developpeurs only) ?</li>
                <li>Comment interagiront les plateformes de différents niveaux (locales, régionales, nationales) ?</li>
                <li>Qui finance ces plateformes ?</li>
                <li>À partir de quelle phase privatise-t-on le service ?</li>
            </ul>
        </p>
        <p>
            S’il convient de garder ces questions en tête,
            elles ne devraient pourtant pas servir d’alibi à l’inaction.
            Pour <strong>Hugues Aubin</strong>, chargé de mission TIC à Rennes,
            plutôt que s’attarder sur la normalisation des données
            (<a href="http://www.groupechronos.org/fre/blog/donnees-publiques.-un-gisement-nomme-desir">pointée du doigt à la FING lors de la conférence du 11 février sur l’ouverture des données</a>),
            partons des bases existantes pour avancer.
        </p>
        <p>
            Et justement de l’avancement
            il y en a avec la plateforme <a href="http://www.data-publica.com" title="Site de Data Publica">Data Publica</a>
            dont la fonction correspond tout à fait à la définition de l’État lab.
        </p>
        <blockquote>
            Data Publica va mettre en place et opérer une place de marché
            sur laquelle ceux qui possèdent des données viendront publier
            leurs méta-données ou leurs API
            ainsi que la ou les licences sous lesquelles elles peuvent être utilisées,
            et ceux qui développent des applications viendront prendre ces méta-données
            pour développer des applications aux modèles économiques
            compatibles avec la licence d’utilisation.
            Data Publica fournira aux éditeurs de données une palette de licences
            parmi lesquelles ils pourront choisir et des outils pour publier leurs données et méta données.
            Data Publica fournira aux dévelopeurs
            un annuaire des API et meta données et l’accès aux données.
            Cette place de marché contribuera au développement des applications
            valorisant les données publiques ou privées.
            Le projet est financé partiellement dans le cadre de l’appel à projet « Web 2.0 innovant ».
            Il est développé par un consortium de trois entreprises : Araok, Nexedi et Talend.
        </blockquote>
        <p>
            Au fur et à mesure des avancées dans la révolution de l’ouverture des données publiques,
            les acteurs tendent à se positionner derrière deux camps.
            <strong>Data Publica rejoindrait les <a href="http://fr.wikipedia.org/wiki/Montagne_%28R%C3%A9volution_fran%C3%A7aise%29">Montagnards</a></strong>
            misant sur la centralisation.
            Leur plateforme semble prendre la voie d’un service exclusif aux développeurs,
            avec catalogue de données brutes,
            approche assez administrative,
            données en partie payantes et
            peu ou pas d’outils intégrés pour la visualisation
            ce qui rend ces données innaccessibles au citoyen lambda.
        </p>
        <p>
            Les plateformes locales
            (<a href="http://www.web-patron.com/2010/02/11/keolis-ouvre-ses-donnees/">Keolis et la ville de Rennes à suivre</a>)
            sont en train de se constituer de manière plus rapide &mdash; relativement parlant &mdash; et polymorphe.
            Elles pourraient prendre une approche plus ludique
            et aussi accessible au public en proposant des outils de visualisation,
            de personnalisation et de comparaison.
            Ce seraient les <a href="http://fr.academic.ru/dic.nsf/frwiki/670662">Girondins</a>
            de l’ouverture des données, proposant un service décentralisé.
        </p>
        <p><strong>Ces approches vont-elles s’entrechoquer ou au contraire devenir complémentaires ?</strong></p>
        <p>
            Dans un article comparant
            la plateforme américaine <a href="http://www.data.gov/catalog">Datagov</a>
            et la plateforme anglaise <a href="http://data.gov.uk/data">datagov.uk</a>,
            Flowing Data indique
            sa <a href="http://flowingdata.com/2010/02/04/data-gov-uk-versus-data-gov-which-wins/">préférence pour la version anglaise</a>,
            plus accessible,
            elle est plus démocratique.
            Un catalogue centrale des données est pourtant indispensable aussi pourquoi ne pas préparer dès aujourd’hui la conciliation des deux versions ?
        </p>
        <p>Data Publica pourrait être le centre de référencement national pour trouver une donnée et l’en extraire pour les développeurs. Les plateformes locales permettraient l’accessibilité aux données par les développeurs mais aussi les citoyens, ce qui serait d’autant plus judicieux qu’il s’agira de données locales fortement plebiscitée: transport, tourisme, etc. Pour s’y retrouver et obtenir un service homogène, il faudrait d’ores et déjà mettre en place des règles, notamment un intitulé-type pour les sites de bases de données: ville.donnee.fr par exemple.</p>
        <p><strong>Et si on changeait l’équation Girondins vs Montagnards pour : Girondins + Montagnards ?</strong></p>
