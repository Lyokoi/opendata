        <p>
            Surmonter ces trois obstacles est indispensable
            si l’on veut que les données soient au cœur de la prochaine révolution internet.
            Cette évolution nécessiterait aussi que les données soient plus acceptées culturellement parlant,
            c’est-à-dire que nous comprenions que ce n’est pas parce qu’une information est dans une base de données
            qu’elle est nécessairement difficile à comparer ou utiliser.
            Il serait urgent de s’attaquer à ce problème,
            et nous sommes confiant qu’il sera résolu très bientôt.
        </p>

