        <p>
            Pour que les données jouent vraiment un rôle sur Internet et dans l'espace public au sens large,
            il reste trois problèmes à résoudre :
            le format des données, la facilité d'y accéder et la possibilité pour chacun de jouer avec.
        </p>
        <p>
            Il semblerait qu’une grande partie de la population pense
            que les données sont loin d’être devenues accessibles à tous.
            Les données les plus intéressantes seraient cachées dans des datacenters très protégés.
            L’argument ultime est que même si les données étaient disponibles,
            rien n’indique qu’elles seraient utilisées par tous.
        </p>
        <p>
            Ces arguments sont en partie vrais.
            Nous les avons résumés en listant les principaux nœuds restant à défaire
            avant que l’on assiste à une véritable appropriation des données par le grand public,
            et à leur utilisation par le plus grand nombre.
        </p>
        
        <h2>1. Trouver un standard commun</h2>

        <p>
            Les données sont encore stockées dans une foultitude de formats différents.
            Des essais ont déjà été faits pour standardiser les données dans des formats communs,
            mais le moins que l’on puisse dire est que ces efforts n’ont encore rien donné.
        </p>
        <p>
            Résultat : quiconque veut utiliser des données doit d’abord les reformater pour son usage personnel.
            Ca fait partie du boulot que fait data.gov, entre autres.
            On a plusieurs raisons d’être optimistes.
            Non pas que les données vont devenir standardisées,
            mais plutôt qu’on dispose de plus en plus d’outils
            nous permettant de passer d’un format à l’autre automatiquement.
        </p>
        <p>
            Sur un autre sujet, et contrairement aux idées reçues,
            les données sont largement disponibles.
            On peut avoir accès à tout types de données sur la circulation,
            la météo,
            la pollution,
            la population,
            les exportations/importations de marchandises,
            l’énergie etc.
            Sur ce sujet, lisez l’article The Data Deluge de The Economist.
        </p>

        <h2>2. Faciliter l’accès aux données</h2>

        <p>
            Bien qu’il y ait des quantités quasi-illimitées de données sur le web,
            les trouver n’est jamais facile.
            Il n’y a pas de véritable moteur de recherche ou de “supermarché de la donnée”
            pour les indexer ou les entreposer.
            Quelques initiatives, la plus connue étant data.gov, tentent de résoudre le problème,
            mais on n’a toujours pas de Datapedia.
        </p>

        <h2>3. Rendre les données divertissantes</h2>

        <p>
            La donnée, c’est souvent ennuyeux.
            Rechercher ses amis sur Google ou Twitter reste beaucoup plus divertissant.
            Très peu de gens passent leurs weekends à jouer avec des séries de données sur Excel.
            Pour que chacun puisse jouer avec des données,
            on a besoin d’interfaces très faciles à utiliser,
            qui devront plaire tout autant à notre cerveau droit (l’émotionnel) qu’au gauche (le rationnel).
        </p>
        <p>
            Ca veut dire que de nouveaux outils doivent faire leur apparition,
            et qu’ils soient révolutionnaires par rapport à ce qui existe aujourd’hui.
            Surtout, ça signifie que l’ère des données sur tableur doit se finir,
            pour être remplacée par celles des données visualisées, ou, en d’autres termes,
            à une ère de représentation graphique.
        </p>
